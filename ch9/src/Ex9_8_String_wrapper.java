public class Ex9_8_String_wrapper {

    public static void main(String[] args) {
        System.out.println("i="+Integer.parseInt("100"));
        System.out.println("i2="+Integer.parseInt("100",2));
        System.out.println("i8="+Integer.parseInt("100",8));
        System.out.println("i16="+Integer.parseInt("ff",16));
    }
}
